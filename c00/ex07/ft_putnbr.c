#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	temp;

	if (nb == -2147483648)
	{
		write(1, "-", 1);
		write(1, "2", 1);
		nb = 147483648;
	}
	if (nb >= 0 && nb < 10)
	{
		temp = nb + '0';
		write (1, &temp, 1);
	}
	else if (nb >= 10 && nb <= 2147483647)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb < 0 && nb >= -2147483647)
	{
		write(1, "-", 1);
		ft_putnbr(nb * -1);
	}
}
